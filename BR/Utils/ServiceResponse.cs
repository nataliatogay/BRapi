using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace BR.Utils
{
    public class ServiceResponse
    {
        public ServiceResponse(StatusCodeService statusCode)
        {
            StatusCode = statusCode;
        }

        public StatusCodeService StatusCode { get; set; }
    }

    public class ServiceResponse<T> : ServiceResponse
    {
        public ServiceResponse(StatusCodeService statusCode, T data) : base(statusCode)
        {
            Data = data;
        }

        public ServiceResponse(T data) : base(StatusCodeService.Ok)
        {
            Data = data;
        }

        public T Data { get; set; }
    }

    public enum StatusCodeService
    {
        Ok,
        SendOnConfirmation,
        SendingNotificationError,
        NotAvailable,
        Error,


        IncorrectVerificationCode,
        CodeHasAlreadyBeenSent,
        Expired,
        SendingMessageError,
        LinkHasAlreadyBeenSent,
        SendingMailError,
        UserNotFound,
        IncorrectLoginOrPassword,
        UserBlocked,
        UserRegistered,
        EmailUsed,
        TokenError,
    }
}
